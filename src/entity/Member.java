package entity;

/**
 * @author Administrator
 *会员实体类
 */
public class Member {
	private int memberId;          //会员编号
	private String memberEmail;    //邮箱
	private String nickName;       //昵称
	private String memberPwd;      //登录密码
	private String pwdQuestion;   //密码问题
	private String pwdAnswer;     //问题答案
	private String memberName;    //真实姓名
	private String gender;        //性别
	private String phone;         //联系电话
	private String address;       //地址
	private String joNumberime;       //注册时间
	private String memberStatus;        //用户状态
	
	
	@Override
	public String toString() {
		return "Member [memberId=" + memberId + ", memberEmail=" + memberEmail
				+ ", nickName=" + nickName + ", memberPwd=" + memberPwd
				+ ", pwdQuestion=" + pwdQuestion + ", pwdAnswer=" + pwdAnswer
				+ ", memberName=" + memberName + ", gender=" + gender
				+ ", phone=" + phone + ", address=" + address
				+ ", joNumberime=" + joNumberime + ", memberStatus="
				+ memberStatus + "]";
	}

	public Member() {
		super();
	}
	
	public Member(int memberId, String memberEmail, String nickName,
			String memberPwd, String pwdQuestion, String pwdAnswer,
			String memberName, String gender, String phone, String address,
			String joNumberime, String memberStatus) {
		super();
		this.memberId = memberId;
		this.memberEmail = memberEmail;
		this.nickName = nickName;
		this.memberPwd = memberPwd;
		this.pwdQuestion = pwdQuestion;
		this.pwdAnswer = pwdAnswer;
		this.memberName = memberName;
		this.gender = gender;
		this.phone = phone;
		this.address = address;
		this.joNumberime = joNumberime;
		this.memberStatus = memberStatus;
	}

	public int getMemberId() {
		return memberId;
	}
	public void setMemberId(int memberId) {
		this.memberId = memberId;
	}
	public String getMemberEmail() {
		return memberEmail;
	}
	public void setMemberEmail(String memberEmail) {
		this.memberEmail = memberEmail;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public String getMemberPwd() {
		return memberPwd;
	}
	public void setMemberPwd(String memberPwd) {
		this.memberPwd = memberPwd;
	}
	public String getPwdQuestion() {
		return pwdQuestion;
	}
	public void setPwdQuestion(String pwdQuestion) {
		this.pwdQuestion = pwdQuestion;
	}
	public String getPwdAnswer() {
		return pwdAnswer;
	}
	public void setPwdAnswer(String pwdAnswer) {
		this.pwdAnswer = pwdAnswer;
	}
	public String getMemberName() {
		return memberName;
	}
	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getJoNumberime() {
		return joNumberime;
	}
	public void setJoNumberime(String joNumberime) {
		this.joNumberime = joNumberime;
	}
	public String getMemberStatus() {
		return memberStatus;
	}
	public void setMemberStatus(String memberStatus) {
		this.memberStatus = memberStatus;
	}
	
	
}
